                <br><br>
                <b>Artículo Noveno:</b> Cuando se haya estipulado suma de dinero como precio del coito, el propietario del semental no entregará el certificado del mismo hasta que reciba el precio.
                <br><br>    
                <b>Artículo Décimo:</b> Si el propietario del semental admite el servicio de éste sin antes haber preestablecido el precio, le esta permitido devolver la perra contra reembolso del precio del coito, gastos de porte y gastos extraordinarios citados en el artículo 3°.
                <br><br>
                <b>Artículo Undécimo:</b> Cuando por una razón cualquiera distinta a la del coito fortuito, la perra no ha podido ser cubierta por el semental elegido, el propietario del semental sólo tiene derecho a los gastos de porte y los gastos extraordinarios citados en el artículo 3°.
                <br><br>
                <b>Artículo Duodécimo:</b> Si la perra apareada no queda preñada, el propietario del semental conserva sus derechos y la cantidad que se hubiere convenido entre la partes.
                <br><br>
                <b>Artículo Decimotercero:</b> No obstante, el propietario de la perra o su derecho habiente puede pedir, para la misma perra, el apareamiento con el mismo semental durante la primera o segunda época de celo subsiguiente, según prefieran, sin tener que pagar de nuevo. Si el nuevo apareamiento no surte efecto, el derecho no vuelve a renovarse. Cesa también por muerte o incapacidad del semental.
                <br><br>
                <b>Artículo Decimocuarto:</b> Si durante este intervalo, el propietario del semental cede al perro, debe imponer al cesionario las obligaciones del artículo anterior. El cedente no garantiza el cumplimiento de esa obligación y sólo está obligado a demostrar que ha impuesto la cláusula. Si no lo ha hecho así o el cesionario no consiente en la obligación gratuitamente, el cedente debe reembolsar el precio del apareamiento recibido.
