                <b>Artículo Tercero:</b> Lo mismo sucede con los gastos extraordinarios que pueda adelantar el propietario del semental en beneficio de la perra, tales como serían los gastos de enfermedad, gastos de estancia anormal tras el coito, gastos de embalaje, etc.
