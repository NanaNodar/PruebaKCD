                <br><br>
                <b>Artículo Vigesimoquinto:</b> Si se cede la perra antes del parto o la camada antes de la fecha fijada para la elección, el cedente debe imponer las obligaciones precedentes al cesionario. El cedente no garantiza el cumplimiento de estas obligaciones por parte del cesionario sino que solo está obligado a justificar que ha impuesto estas obligaciones.
                <br><br>
                <b>Artículo Vigesimosexto:</b> Si es cedente descuida estas obligaciones y si por razón el cesionario no permite la elección gratuita, el propietario del semental tiene derecho, como compensación, al doble del precio normal del apareamiento de un semental de reputación equivalente a la del suyo. En caso de no llegarse a un acuerdo en la determinación del precio, éste será fijado por un árbitro.
                <br><br>
                <b>Artículo Vigesimoséptimo:</b> Si lo prefiere, el propietario del semental puede exigir el precio normal simple, fijado eventualmente por un árbitro, si por causa del propietario de la hembra reproductora (cesación, cambio de residencia, etc.), se viese obligado a elegir en otro país o a una distancia superior en 100 Km. a la que existía entre la residencia de ambos propietaria en el momento del apareamiento.
                <br><br>
                <b>Artículo Vigesimoctavo:</b> Cuando el apareamiento se acuerda a la vez mediante pago de una cantidad y elección de un cachorro, el propietario del semental, salvo que se estipule lo contrario, tiene el derecho a elegir en segundo lugar. En este caso, las reglas más arriba indicadas, referentes al pago, por una parte, y a la selección, por otra, deben observarse con las condiciones siguientes:

