                <h3 style="margin-top: 60px;">CONSENSO DE MÓNACO - 1943</h3>
                <p><b>Artículo Primero:</b> La perra viaja por cuenta y gasto de su propietario. El propietario del macho debe a la perra, mientras esté confinada a su cuidado, todo cuidado que un criador concienzudo da a sus perros.
