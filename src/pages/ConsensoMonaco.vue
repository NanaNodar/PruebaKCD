                <b>Artículo Decimoquinto:</b> En caso de venta del semental o cambio de residencia que exija un nuevo apareamiento, un desplazamiento a otro país o una distancia superior en 100 km. a la que existía entre la residencia de los dos propietarios en el momento del primer apareamiento, el propietario de la perra puede, si lo prefiere, renunciar a un nuevo apareamiento y rembolsarse la mitad de la cantidad entregada al propietario por el apareamiento infructuoso.
