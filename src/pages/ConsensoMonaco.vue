                <b>Artículo Cuarto:</b> Sin consenso formal del propietario de la perra, el semental convenido no podrá ser sustituido por otro.
                <br><br>    
                <b>Artículo Quinto:</b> En caso de coito fortuito con un semental distinto del elegido, antes o después del servicio de este último, el propietario del semental tiene todas las obligaciones habituales y ningún derecho. No está obligado a otras atenciones especiales por el cito fortuito, salvo el reembolso de los gastos del coito y del viaje de la perra. En este caso, los derechos y los deberes de las partes son los habituales. El segundo coito deberá retrasarse cuanto se pueda, si es posible varios días.
                <br><br>
                <b>Artículo Sexto:</b> Cuando la perra haya sido cubierta por sementales diferentes, el certificado del coito mencionará a los distintos sementales y las fechas de los servicios.
                <br><br>
                <b>Artículo Séptimo:</b> El coito solo se repetirá si media convenio formal.
                <br><br> 
