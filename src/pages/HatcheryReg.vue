<template>
    <section class="reg_crianza">
        <div class="reg_crianza_container">
            <h2 class="reg_crianza_content title">Reglamento de Crianza y Criaderos</h2>
            <div class="reg_crianza_content paragraph">
               <p>El Kennel Club Digital establece este Reglamento para guiar y regular la cría de perros internacional. Nuestro compromiso es promover el bienestar animal, la mejora genética y la ética en la cinofilia. Este documento abarca la inscripción de criaderos, el cumplimiento de las Normas de Excelencia y el respeto al Manual y Libreta de Bienestar Animal.
                <br><br>
               Estas normativas fomentan prácticas responsables y éticas entre los criadores, contribuyendo a la preservación de las razas caninas y al cuidado de nuestros fieles compañeros. Este reglamento refleja nuestro compromiso con la cinofilia de excelencia y el bienestar de los perros en Argentina.

               <hr>

                <h3>Título I: Disposiciones Generales</h3>

                <b>Artículo 1:</b> La Dirección de Registro Genealógico del Kennel Club Digital, administra, controla y resuelve todo lo atinente a los criaderos registrados en el mismo.
                <br><br>
                <b>Artículo 2:</b> Inspectores y Control de Criaderos
                <br>
                La Dirección de Registro Genealógico podrá designar Inspectores para ejercer un control directo cuando la situación lo requiera. La inscripción de un criadero, en el Registro del Kennel Club Digital, lleva implícita la aceptación de las inspecciones que disponga dicha Dirección.
                <br><br>
                <b>Artículo 3:</b> Condiciones para Ser Considerado Criador
                <br>
                Toda persona que inscriba una lechigada en el Registro Genealógico será considerada automáticamente como criador y como tal deberá inscribir su criadero en el Registro de Criaderos del Kennel Club Digital.
                <br><br>
                <b>Artículo 4:</b> Inscripción y Registro de Nombres de Criadero
                <br>
                La solicitud para la inscripción de un nombre de Criadero deberá ser presentada por escrito a la Dirección de Registro Genealógico, en los <router-link to="/inscripcion-criadero" target="_blank">formularios oficiales online</router-link> . El Criador indicará 3 (tres) nombres opcionales en la eventualidad de que alguno de ellos no pudiese ser registrado. La terna de nombres tendrá que tener orden de prelación. El registro y ulterior utilización por parte del titular del Criadero será aceptada o denegada dentro de los quince días siempre y cuando reúna los siguientes requisitos:
                <ol>
                    <li>El nombre podrá ser en cualquier idioma, e incluso palabras sin expreso significado, siempre y cuando no se consideren contrarios a la ley, a la moral o las buenas costumbres.</li>
                    <li>No podrán ser utilizados nombres de Criaderos existentes o desaparecidos de este Registro internacional. A excepción de los autorizados en forma excepcional.</li>
                    <li>A criterio de la CD, no se admitirá el registro de Criaderos cuyos nombres se puedan prestar a confusión con otros ya registrados, por su significado, ortografía o pronunciación fonética.</li>
                    <li>Deberá denunciar un domicilio del criadero. No se admitirá como tal “casillas de correo” o similares.</li>
                </ol>
                <b>Artículo 5:</b> Propiedad de Nombres de Criadero
                 <br>
                 El título del nombre del Criadero será propiedad privada y personal de cada criador y durará toda su vida, siempre y cuando se encuentre activo, según se especifica en el <router-link to="/anexo-categorias-y-duracion-de-criaderos" target="_blank">Anexo I - Categorías y Duración de Criaderos</router-link> . El título de un criadero desaparecido no podrá ser solicitado nuevamente por otra persona y solo podrá ser heredado por su sucesor legítimo según las leyes vigentes. En caso de fallecimiento del titular, sus herederos legítimos deberán acreditar la condición de tales, mediante la presentación de la declaratoria de herederos. Mientras se sustancia el juicio sucesorio, cualquiera de los herederos podrá solicitar autorización judicial para - en calidad de administrador - continuar con los trámites ante esta Institución.
                <br><br>
                <b>Artículo 6:</b> Copropiedad de Criaderos
                <br>
                El o los propietarios del ejemplar hembra son los únicos autorizados para inscribir un criadero, debiendo estar el ejemplar a su nombre. Podrán inscribirse criaderos, en copropiedad, a nombre de dos personas, debiendo ser firmada la solicitud de inscripción por todos los titulares, aún existiendo cualquier grado de parentesco entre ellos. En el mismo formulario de inscripción, los solicitantes deberán dejar constancia de la modalidad de la firma, que obligue al criadero con el Kennel Club Digital y con terceros, la que podrá ser: indistinta por cualquiera de ellos, o en forma conjunta; esta decisión solo podrá ser modificada por escrito con la firma de todos los titulares.
                <br><br>
                <b>Artículo 7:</b> Límite de Nombres de Criadero
                <br>
                Ningún criador estará autorizado a registrar más de un nombre de criadero para la misma raza, pudiendo hacerlo para razas distintas con diferentes nombres; también podrá inscribir otro/s criadero/s en copropiedad. Los criaderos son propiedad privada y podrán ser transferidos de titularidad.
                <br><br>
                <b>Artículo 8:</b> Derecho de Inscripción de Nombres de Criadero
                <br>
                Al registrar un nombre de criadero, cada criador deberá abonar el derecho de inscripción respectivo.
                <br><br>
                <b>Artículo 9:</b> Cambio de Nombre de Criadero
                <br>
                Todo ejemplar registrado en el Registro Genealógico, mantendrá su nombre de criadero de por vida. En caso de ser autorizado para el cambio de nombre deberá volver a abonar la tarifa como “nuevo criadero” y de ser necesario el pago de transferencia de los perros del antiguo criadero.
                <br><br>
                <b>Artículo 10:</b> Obligaciones de los Criadores
                <br>
                Todos los criadores estarán obligados a respetar las siguientes condiciones:
                <ol type="a">
                    <li>Criar con miras al mejoramiento de la/s raza/s a que se dediquen.</li>
                    <li>Registrar todas las lechigadas en el Registro Genealógico, completas.</li>
                    <li>Permitir en cualquier momento la inspección del criadero. El criador que negara la entrada a un inspector identificado por medio de una carta credencial, firmada por un miembro de la C.D., será pasible de la sanción que aplique la Comisión de Disciplina a propuesta de la Dirección de Registro Genealógico.</li>
                    <li>Efectuar publicidad veraz, digna y sin deslealtad hacia los criadores.</li>
                    <li>Comunicar a la Dirección del Registro Genealógico los cambios de domicilio y toda información que se considere de interés.</li>
                    <li>Comunicar la muerte de todo ejemplar por escrito dentro de los 30 (treinta) días de producido el hecho a fin de darle de baja en el Registro Genealógico.</li>
                    <li>Disponer de un lugar apropiado en lo referente a higiene, salubridad y espacio adecuado de acuerdo con el tamaño y funciones de la o las razas que críe. La Dirección del Registro Genealógico deberá agotar todos los medios a su alcance, inclusive recurrir a las normas legales vigentes, para hacer cumplir este artículo.</li>
                    <li>Permitir la extracción de muestras para el análisis de ADN de uno o más ejemplares, que dispongan la Dirección del Registro Genealógico o la Comisión de Disciplina.</li>
                    <li>Concurrir a las citaciones de la Dirección del Registro Genealógico.</li>
                    <li>Realizar el aviso de servicio de sus ejemplares machos a la Dirección del Registro Genealógico; en caso de no realizarla podrá ser intimado a realizarla por la misma Dirección y de reunirse prueba suficiente, este último podrá dictar de oficio el registro de dicho servicio.</li>
                    <li>El incumplimiento de las obligaciones establecidas en los incisos h) e i) hará pasible al incumplidor de la sanción de suspensión del criadero, mientras persista en su negativa o incomparecencia. <br> La Dirección solicitará directamente a la Comisión de Disciplina la aplicación de estas sanciones, que el órgano disciplinario deberá resolver en el plazo perentorio.</li>
                </ol>

                <h3>Título II: Tarifas y Actualizaciones</h3>

                <b>Artículo 11:</b> Actualización de Aranceles
                <br>
                Cada vez que el C. D. lo considere conveniente actualizará los aranceles para el trámite de inscripción de un criadero Internacional.

                <h3>Título III: Cumplimiento de las Normas de Excelencia y Atención al Manual y Libreta de Bienestar Animal</h3>

                <b>Artículo 12:</b> Cumplimiento de las Normas de Excelencia y Atención al Manual y Libreta de Bienestar Animal
                <br>
                Todos los criadores/as registrados en el Kennel Club Digital deben comprometerse a cumplir y promover activamente las <router-link to="/normas-de-excelencia">Normas de Excelencia</router-link>  establecidas por esta institución. <u>Estas normas representan los más altos estándares de calidad en la cinofilia y abarcan aspectos esenciales relacionados con el cuidado, la cría y la exhibición de perros.</u> <br><br>
                Los criadores tienen la responsabilidad de familiarizarse plenamente con las Normas de Excelencia, el Manual y la Libreta de Bienestar Animal, ir incorporando estos principios en sus prácticas de cría y cuidado de los perros. Esto incluye, pero no se limita a:
                <ul>
                    <li>Cumplir con los estándares de salud y bienestar animal establecidos en las Normas de Excelencia en todas las etapas de la cría.</li>
                    <li>Mantener registros de salud y cuidado de sus perros, de acuerdo con las pautas del Manual y la Libreta de Bienestar Animal.</li>
                    <li>Promover activamente el respeto y la adhesión a estas normas entre otros criadores y miembros de la comunidad cinófila.</li>
                    <li>Participar en programas de educación y capacitación sobre bienestar animal, siempre que estén disponibles.</li>
                </ul>

                <h3>Título IV: Sanciones y Modificaciones</h3>

                <b>Artículo 13:</b> Sanciones por Transgresión
                <br>
                Cualquier transgresión al presente reglamento será penada desde el apercibimiento hasta la suspensión parcial o de por vida para criar en el Kennel Club Digital.
                <br><br>
                <b>Artículo 14:</b> Modificaciones al Reglamento
                <br>
                El presente Reglamento podrá ser modificado parcial o totalmente siguiendo las pautas estatutarias vigentes.
                <br><br>
                <b>Artículo 15:</b> Vigencia del Reglamento
                <br>
                El presente reglamento comenzará a regir a partir de su promulgación hasta nuevo aviso.
                </p>
            </div>
            
